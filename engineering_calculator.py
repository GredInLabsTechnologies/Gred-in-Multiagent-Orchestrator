def reynolds_number(rho, v, L, mu):
    """Reynolds number: Re = rho*v*L/mu."""
    return rho * v * L / mu


def bernoulli_pressure(rho, v1, P1, v2):
    """Bernoulli equation: P2 = P1 + 0.5*rho*(v1^2 - v2^2)."""
    return P1 + 0.5 * rho * (v1**2 - v2**2)


def flow_rate(A, v):
    """Volumetric flow rate: Q = A*v."""
    return A * v


def hydraulic_diameter(A, P):
    """Hydraulic diameter: Dh = 4*A/P."""
    return 4.0 * A / P


def stress(F, A):
    """Normal stress: sigma = F/A."""
    return F / A


def strain(dL, L):
    """Strain: epsilon = dL/L."""
    return dL / L


def youngs_modulus(stress_val, strain_val):
    """Young's modulus: E = sigma/epsilon."""
    return stress_val / strain_val


def moment_of_inertia_rect(b, h):
    """Moment of inertia for rectangle: I = b*h^3/12."""
    return (b * h**3) / 12.0


def moment_of_inertia_circle(r):
    """Moment of inertia for circle: I = pi*r^4/4."""
    return (math.pi * r**4) / 4.0


def bending_stress(M, y, I):
    """Bending stress: sigma = M*y/I."""
    return M * y / I


