def ideal_gas_law(P=None, V=None, n=None, T=None):
    """Solve PV = nRT for the missing variable."""
    R = 8.314462618
    if P is None: return n * R * T / V
    if V is None: return n * R * T / P
    if n is None: return P * V / (R * T)
    if T is None: return P * V / (n * R)
    raise ValueError("Provide exactly three of P, V, n, T")


def carnot_efficiency(T_hot, T_cold):
    """Carnot efficiency (temperatures in Kelvin)."""
    return 1.0 - (T_cold / T_hot)


def heat_transfer_conduction(k, A, dT, L):
    """Fourier's law: Q = k*A*dT/L."""
    return k * A * dT / L


def heat_transfer_convection(h, A, dT):
    """Newton's law of cooling: Q = h*A*dT."""
    return h * A * dT


def heat_transfer_radiation(epsilon, A, T):
    """Stefan-Boltzmann radiation: Q = eps*sigma*A*T^4."""
    sigma = 5.670374419e-8
    return epsilon * sigma * A * T**4


