def ohms_law(V=None, I=None, R=None):
    """Solve Ohm's law for the missing variable."""
    if V is None: return I * R
    if I is None: return V / R
    if R is None: return V / I
    raise ValueError("Provide exactly two of V, I, R")


def series_resistance(*resistors):
    """Total resistance in series."""
    return sum(resistors)


def parallel_resistance(*resistors):
    """Total resistance in parallel."""
    return 1.0 / sum(1.0/r for r in resistors)


def rc_time_constant(R, C):
    """RC circuit time constant (tau = R*C)."""
    return R * C


def rl_time_constant(R, L):
    """RL circuit time constant (tau = L/R)."""
    return L / R


def power_electrical(V=None, I=None, R=None):
    """Electrical power from any two of V, I, R."""
    if V is not None and I is not None: return V * I
    if V is not None and R is not None: return V**2 / R
    if I is not None and R is not None: return I**2 * R
    raise ValueError("Provide exactly two of V, I, R")


