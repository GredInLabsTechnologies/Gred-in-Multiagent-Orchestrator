def convert_temperature(value: float, from_unit: str, to_unit: str) -> float:
    """Convert temperature between C, F, K."""
    if from_unit == "C":
        celsius = value
    elif from_unit == "F":
        celsius = (value - 32) * 5 / 9
    elif from_unit == "K":
        celsius = value - 273.15
    else:
        raise ValueError(f"Unknown unit: {from_unit}")
    if to_unit == "C":
        return celsius
    elif to_unit == "F":
        return celsius * 9 / 5 + 32
    elif to_unit == "K":
        return celsius + 273.15
    else:
        raise ValueError(f"Unknown unit: {to_unit}")


def convert_length(value: float, from_unit: str, to_unit: str) -> float:
    """Convert length between m, km, mi, ft, in, cm, mm."""
    to_meters = {"m": 1.0, "km": 1000.0, "mi": 1609.344, "ft": 0.3048, "in": 0.0254, "cm": 0.01, "mm": 0.001}
    if from_unit not in to_meters or to_unit not in to_meters:
        raise ValueError(f"Unknown unit")
    meters = value * to_meters[from_unit]
    return meters / to_meters[to_unit]


def convert_mass(value: float, from_unit: str, to_unit: str) -> float:
    """Convert mass between kg, g, lb, oz, ton."""
    to_kg = {"kg": 1.0, "g": 0.001, "lb": 0.453592, "oz": 0.0283495, "ton": 1000.0}
    if from_unit not in to_kg or to_unit not in to_kg:
        raise ValueError(f"Unknown mass unit")
    kg = value * to_kg[from_unit]
    return kg / to_kg[to_unit]


def convert_pressure(value: float, from_unit: str, to_unit: str) -> float:
    """Convert pressure between Pa, kPa, MPa, bar, atm, psi."""
    to_pa = {"Pa": 1.0, "kPa": 1000.0, "MPa": 1e6, "bar": 1e5, "atm": 101325.0, "psi": 6894.757}
    if from_unit not in to_pa or to_unit not in to_pa:
        raise ValueError(f"Unknown pressure unit")
    pa = value * to_pa[from_unit]
    return pa / to_pa[to_unit]


def convert_energy(value: float, from_unit: str, to_unit: str) -> float:
    """Convert energy between J, kJ, MJ, cal, kcal, BTU, kWh, eV."""
    to_j = {"J": 1.0, "kJ": 1000.0, "MJ": 1e6, "cal": 4.184, "kcal": 4184.0, "BTU": 1055.06, "kWh": 3.6e6, "eV": 1.602176634e-19}
    if from_unit not in to_j or to_unit not in to_j:
        raise ValueError(f"Unknown energy unit")
    joules = value * to_j[from_unit]
    return joules / to_j[to_unit]


